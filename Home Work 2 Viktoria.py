# Arithmetic Operators
x = 15
y = 10
print('x+y=', x+y)

print('+-y', x-y)

print('x*y=', x*y)

print('x/y=', x/y)

print('x%y=', x%y)

print('x**y=', x**y)


# Comparison Operators
x=3
y=7
print('x>y is',x>y)
print('x<y is', x<y)
print('x==y is', x==y)
print('x !=y is', x !=y)
print('x >=y is', x>=y)

# Logical Operators
a = True
b = False
print('a and b is', a and b)
print('a or b is', a or b)
print('not b is', not b)
print('not a is', not a)


